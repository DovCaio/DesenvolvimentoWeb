//Sistemas númericos: binário, octal, hexadecimal.

//Manipulamos os diversos tipos de números com apenas um tipo o Number

let int = 3;
let float = 3.1;

typeof int; //Number
typeof float; //Number

//Os operadores são padrão: +, -, *, / e %

//Temos também a precedencias que é como nas outras linguagens, podemos usar parâmetros para dar maior precedencia
//a uma operação

//Podemos incrementar variáveis

int++; //Retorna o 3 e incremente a variável int e ela passa a ser 4.
++int; //Incrementa a variável int e retorna seu novo valor 5.

//Contece a mesma coisa com o operador de decremento

int--;
--int; 

//Temos diversos operadores de atribuição: =, +=, -=, *=, /=, bem intuitivo o que eles fazem

